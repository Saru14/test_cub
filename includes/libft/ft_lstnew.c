#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	if (!(new = malloc(sizeof(t_list))))
		return (NULL);
	new->content = content;
	new->next = NULL;
	return (new);
}

/*
** malloc et renvoie un nouvel element
** content initialisee a l'aide de la valeur du param content
** next initialisee a NULL
*/
